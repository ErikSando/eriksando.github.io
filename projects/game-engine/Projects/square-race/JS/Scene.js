function LoadTilemap(tilemap = [[]], tilesize = 60) {
    let scene = new Scene();

    let x, y = 0;

    for (let i = 0; i < tilemap.length; i++) {
        x = 0;

        for (let j = 0; j < tilemap[i].length; j++) {
            let tile = tilemap[i][j];

            if (tile == 1) {
                let gameObject = new GameObject(new Vector(x, y), new Vector(tilesize, tilesize), true);
                gameObject.colour = borderColour;

                scene.Add(gameObject);
            }
            else if (tile == 2) {
                let gameObject = new GameObject(new Vector(x, y), new Vector(tilesize, tilesize), true);
                gameObject.colour = victoryColour;
                gameObject.tag = "victory";

                scene.Add(gameObject);
            }
            else if (tile == 3) {
                squareStart = new Vector(x, y);
            }

            x += tilesize;
        }

        y += tilesize;
    }

    return scene;
}

